# 4. Write a program that uses a variable to store a word, then prints that word in reverse order.

word = "sky"
print (word[::-1])

# word[::-1] uses Python's string slicing feature:
# The first colon : means start from the beginning
# The second colon : means go until the end
# The -1 means move backward one character at a time

